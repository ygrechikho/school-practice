public class Student {
    private String surname;
    private double grade;

    public double getGrade() {
        return grade;
    }

    public void setGrade(double newGrade) {
        this.grade = newGrade;
    }

    public String getName() {
        return surname;
    }

    public void setName(String newName) {
        this.surname = newName;
    }
}
